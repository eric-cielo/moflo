const __dirname = dirname(fileURLToPath(import.meta.url));

// Single source of truth for the launcher's guidance-mirror header. Section 3
// (full sync) and section 3b (restore + prune) both depend on this string —
// if they drift, the prune stops recognizing its own files and stale mirrors
// linger forever (#839 was exactly this failure mode).
const SESSION_START_MIRROR_MARKER = '<!-- AUTO-GENERATED by moflo session-start.';
function sessionStartMirrorHeader(file) {
  return `${SESSION_START_MIRROR_MARKER} Do not edit — changes will be overwritten. -->\n<!-- Source: node_modules/moflo/.claude/guidance/shipped/${file} -->\n\n`;
}

// IMPORTANT: Do NOT use resolve(__dirname, '..') or '../..' — this script lives
// in bin/ during development but gets synced to .claude/scripts/ in consumer
// projects, so __dirname-relative paths break. findProjectRoot() (lib/moflo-
// paths.mjs) resolves identically to the TS bridge (#1057): CLAUDE_PROJECT_DIR
// first, then walk up for .moflo/moflo.db / .swarm/memory.db / CLAUDE.md+pkg /
// package.json / .git. Inline walks here have caused N writers to land on
// different DBs than the bridge reads from — never reintroduce one.
const projectRoot = findProjectRoot();

// Monorepo nested-.moflo guard (#1174). After the resolver fix, findProjectRoot
// returns the topmost ancestor with .moflo/moflo.db, so an ancestor still
// having one of those files means CLAUDE_PROJECT_DIR pinned us inside a
// sub-workspace, or someone reintroduced an inline walk-up. Either way, the
// daemon spawned from here would NOT be the same one a sibling cwd in the
// same monorepo resolves to. Emit a clear stderr warning so the user can run
// `flo doctor --fix` to consolidate.
try {
  const ancestor = findAncestorMofloRoot(projectRoot);
  if (ancestor) {
    process.stderr.write(
      `moflo: nested .moflo/ detected — using "${projectRoot}" while ancestor "${ancestor}" also has .moflo/moflo.db. ` +
      `This fragments monorepo state across multiple daemons (#1174). Run "flo doctor --fix" to consolidate.\n`,
    );
  }
} catch { /* diagnostic-only — must never block session start */ }

// Dogfood guard (#928). When this launcher runs inside the moflo repo itself,
// .claude/scripts/, .claude/helpers/, and .claude/guidance/ are committed git
// files — they ARE moflo's source of truth, not destinations to be re-synced
// from node_modules. Drift heal would silently revert any post-publish edit
// to one of those files (e.g. story #927's gate.cjs got reverted overnight
// because manifest.size still pointed at the previously-published version).
// Detection is `package.json#name === "moflo"` — the project's own package.json,
// NOT node_modules/moflo/package.json. Defaults to false on any read/parse
// error so a corrupt package.json never silently disables drift heal in a
// real consumer.
//
// Workspace caveat: findProjectRoot() walks up to the nearest package.json,
// so in a workspace child (packages/foo/) the read sees the child package
// (name !== "moflo") and the guard stays false. moflo isn't a workspace
// today; if it ever becomes one, run sessions from the repo root or extend
// this check to walk further up.
let isMofloDogfood = false;
try {
  const projectPkgPath = resolve(projectRoot, 'package.json');
  if (existsSync(projectPkgPath)) {
    const projectPkg = JSON.parse(readFileSync(projectPkgPath, 'utf-8'));
    isMofloDogfood = projectPkg?.name === 'moflo';
  }
} catch (err) {
  // Defaults to false — safer than accidentally disabling drift heal in a
  // real consumer. Surface the failure so a corrupt project package.json
  // doesn't silently change launcher behavior (per feedback_no_silent_failures).
  process.stderr.write(`[moflo] dogfood-guard package.json read failed: ${err && err.message ? err.message : String(err)}\n`);
}

// Visible mutation reporter (#716). Claude Code's SessionStart hook captures
// stdout as `additionalContext`, so each line here surfaces to Claude — and
// through it to the user — explaining what the launcher just changed. Keep
// silent fast-path: only call this when something actually mutated.
//
// `mutationCount` is read by the post-spawn notice writer (#636) so the
// statusline notice + the closing "starting background tasks" line both
// know whether anything actually fired this session.
let mutationCount = 0;
function emitMutation(action, details) {
  mutationCount++;
  try {
    const tail = details ? ` (${details})` : '';
    process.stdout.write(`moflo: ${action}${tail}\n`);
  } catch {
    // Writing must never throw — a broken stdout would block session start.
  }
}

// Stderr counterpart to emitMutation for non-fatal failures (#854). Every
// previously-bare `catch {}` in the upgrade flow is routed through here so
// partial-migration regressions can never go silent again. The inner try
// guards against a broken stderr pipe — writing the failure itself must
// never throw, otherwise a fast session-end would surface as a crash.
function emitWarning(message) {
  try {
    process.stderr.write(`moflo: ${message}\n`);
  } catch { /* stderr write must not throw */ }
}
function errMessage(err) {
  return err && err.message ? err.message : String(err);
}

// Post-upgrade monorepo consolidation notice (#1174). The resolver change in
// 4.10.13+ flips project-root resolution from "nearest .moflo/" to "topmost
// .moflo/". Consumers with pre-fix nested .moflo/ residue will see their
// effective projectRoot move upward, orphaning sub-daemons and changing the
// per-project daemon port hash. Write a one-time restart-pending.json so the
// user (via Claude's CLAUDE.md surface-and-delete rule) sees the guidance and
// runs `flo doctor --fix -c nested-moflo` before the gate hooks start failing.
//
// Sentinel state-machine — re-arms after consolidation:
//   !sentinel && nested  → notify (write json + sentinel, emit mutation)
//   sentinel  && nested  → silent (already notified)
//   sentinel  && !nested → cleanup sentinel (user consolidated; re-arm)
//   !sentinel && !nested → silent (nothing to do)
//
// Best-effort — never blocks session start.
try {
  const nestedNoticeSentinel = join(mofloDir(projectRoot), 'nested-moflo-notice-shown');

  // Cheap, depth-1 downward walk: just check the immediate children of
  // projectRoot for `.moflo/moflo.db`. The full `flo doctor` BFS is depth-5
  // — that's for the diagnostic. Here we only need a fast signal so the
  // launcher stays under its perf budget.
  //
  // Skip-list is shared with the doctor BFS via COMMON_WALK_SKIP_NAMES to
  // prevent divergence. Only `.moflo*`-prefixed dirs are filtered separately
  // — a `.packages/` or `.workspaces/` directory with its own moflo state is
  // legitimate (rare, but possible) and should still be detected.
  let firstNested = null;
  try {
    const entries = readdirSync(projectRoot, { withFileTypes: true });
    for (const entry of entries) {
      if (!entry.isDirectory()) continue;
      const name = entry.name;
      if (COMMON_WALK_SKIP_NAMES.has(name.toLowerCase())) continue;
      if (name.toLowerCase().startsWith('.moflo')) continue;
      const childDir = join(projectRoot, name);
      if (existsSync(join(childDir, '.moflo', 'moflo.db'))) {
        firstNested = childDir;
        break;
      }
    }
  } catch { /* depth-1 walk failure — fall through, no notice */ }

  const sentinelExists = existsSync(nestedNoticeSentinel);

  if (firstNested && !sentinelExists) {
    try {
      mkdirSync(mofloDir(projectRoot), { recursive: true });
      const pendingPath = join(mofloDir(projectRoot), 'restart-pending.json');
      writeFileSync(pendingPath, JSON.stringify({
        // schemaVersion (not the moflo package version) lets future readers
        // branch on the notice shape if it ever evolves. The launcher's §0d
        // version-match cleanup compares `pending.version === installedVersion`
        // (moflo semver), so this schema field is safely ignored there.
        schemaVersion: 1,
        kind: 'nested-moflo',
        message:
          `moflo consolidates monorepo state at the repo root (#1174). Nested .moflo/ ` +
          `directories detected (e.g. ${firstNested}). Run \`flo doctor --fix -c nested-moflo\` ` +
          `to archive them and stop any orphaned sub-daemons.`,
        createdAt: new Date().toISOString(),
      }, null, 2));
      writeFileSync(nestedNoticeSentinel, new Date().toISOString());
      emitMutation('nested .moflo/ detected — restart-pending.json written with consolidation guidance');
    } catch (err) {
      emitWarning(`nested-moflo notice write failed: ${errMessage(err)}`);
    }
  } else if (!firstNested && sentinelExists) {
    // User has consolidated. Clear the sentinel so a future re-introduction
    // of nested .moflo/ re-arms the notice path.
    try {
      unlinkSync(nestedNoticeSentinel);
    } catch { /* may have just been removed concurrently */ }
  }
} catch { /* diagnostic-only — must never block session start */ }

// Manifest schema (#854 hardening). Originally `string[]`; now `{path,size}[]`
// so the launcher can detect *content* drift, not just *missing-file* drift.
// Reading accepts both forms — a legacy v1 manifest is reported via
// `isLegacy=true` so the drift check forces one re-sync to migrate to v2,
// closing the failure mode where a v4.9.2 launcher writes a stamp+manifest
// in stage 1 of an upgrade and the v4.9.3 launcher (with this fix) sees
// `installedVersion === cachedVersion` + no file-missing drift, then skips
// section 3 leaving stale `gate.cjs` etc. stuck.
function readInstallManifest(manifestPath) {
  let raw;
  try { raw = readFileSync(manifestPath, 'utf-8'); } catch { return { entries: [], isLegacy: false }; }
  let parsed;
  try { parsed = JSON.parse(raw); } catch { return { entries: [], isLegacy: false }; }
  if (!Array.isArray(parsed)) return { entries: [], isLegacy: false };
  let isLegacy = false;
  const entries = [];
  for (const item of parsed) {
    if (typeof item === 'string') {
      isLegacy = true;
      entries.push({ path: item, size: null });
    } else if (item && typeof item === 'object' && typeof item.path === 'string') {
      entries.push({ path: item.path, size: typeof item.size === 'number' ? item.size : null });
    }
    // malformed entries are silently dropped — not surfaceable, never written by us
  }
  return { entries, isLegacy };
}

const plural = (n, word) => `${n} ${word}${n === 1 ? '' : 's'}`;

// Captured inside the upgrade/drift branch so the post-spawn notice writer
// can persist `.moflo/upgrade-notice.json` for the statusline (#636).
let upgradeNoticeContext = null;

// Deferred so we commit it AFTER every upgrade-work block (see 3g). The stamp
// is the "launcher fully completed" signal — writing it mid-flight lets an
// aborted launcher strand consumers on a half-applied upgrade (#730).
let pendingVersionStampWrite = null;

// 5-min TTL is a safety net for zombie launchers (statusline ignores past-TTL
// files). The 2-min "completed" TTL lets the user see the post-upgrade badge
// briefly in the next session render (Claude Code renders the statusline only
// AFTER the SessionStart hook returns, so the in-progress badge has effectively
// zero visibility window). The next session-start's section 0-pre wipes any
// leftover, so a stale completed notice can't linger past one session.
const UPGRADE_NOTICE_INPROGRESS_TTL_MS = 5 * 60 * 1000;
const UPGRADE_NOTICE_COMPLETED_TTL_MS = 2 * 60 * 1000;
const UPGRADE_NOTICE_PATH = () => join(mofloDir(projectRoot), 'upgrade-notice.json');

// Single-source-of-truth notice writer. Reused by writeUpgradeNotice (the
// version-bump / drift-heal path) and the §0-bootstrap-sentinel + §3h paths
// (#975 statusline-channel promotion). Keeps the JSON shape colocated with
// the TTL constants instead of letting it drift across two inline copies.
function buildAndWriteNotice(context, status) {
  if (!context) return;
  const ttlMs = status === 'completed'
    ? UPGRADE_NOTICE_COMPLETED_TTL_MS
    : UPGRADE_NOTICE_INPROGRESS_TTL_MS;
  try {
    mkdirSync(mofloDir(projectRoot), { recursive: true });
    const now = Date.now();
    const notice = {
      status,
      kind: context.kind,
      from: context.from,
      to: context.to,
      at: new Date(now).toISOString(),
      expiresAt: new Date(now + ttlMs).toISOString(),
      changes: 0,
    };
    writeFileSync(UPGRADE_NOTICE_PATH(), JSON.stringify(notice, null, 2));
  } catch { /* non-fatal — statusline just won't show the segment */ }
}

function writeUpgradeNotice(status) {
  buildAndWriteNotice(upgradeNoticeContext, status);
}

// ── 0-pre. Drop any stale upgrade notice (#738, #743, #1173) ────────────────
// `upgrade-notice.json` is a transient handshake between launcher and
// statusline — it should never survive past the launcher run that wrote it.
// Pre-#738 launchers wrote a 1-hour-TTL "complete" notice after upgrade work
// finished; with the #738 contract that file can only be a leftover, but the
// statusline still rendered it for the rest of the hour. Unconditionally
// removing it here makes the contract self-healing — any future zombie
// notice (legacy file, aborted launcher, future writer mistake) gets dropped
// before the statusline can see it. The in-progress notice for THIS session,
// if any, is written later in section 3 and cleared in section 3f.
//
// #1173: capture the prior notice's parsed contents BEFORE deleting so §3 can
// detect "prior session completed upgrade work but never committed the stamp"
// and recover via eager-stamp (Option B). Without this read, the indefinite
// re-detect loop reported in #1173 has no signal to break on. Wrapped in an
// existsSync guard so the common no-upgrade path doesn't pay for an ENOENT
// throw + stack unwind on every session-start.
let priorUpgradeNotice = null;
if (existsSync(UPGRADE_NOTICE_PATH())) {
  try {
    priorUpgradeNotice = JSON.parse(readFileSync(UPGRADE_NOTICE_PATH(), 'utf-8'));
  } catch { /* unparseable — fall through; treat as no signal */ }
  try {
    unlinkSync(UPGRADE_NOTICE_PATH());
  } catch { /* deleted between stat and unlink — fine */ }
}

// #1173 Option D: defensive cleanup of in-progress upgrade notice if the
// launcher aborts before §3f writes 'completed'. Without this, a mid-flight
// abort leaves the (updating…) badge on the statusline until the 5-min TTL
// expires, even though no upgrade is actually in progress.
//
// Coverage matrix:
//   - normal exit / process.exit() / uncaught exception → 'exit' fires
//   - POSIX SIGTERM/SIGINT (Claude Code's 5s hook-timeout kill, Ctrl+C) →
//     kernel terminates without running 'exit'; explicit handlers cover this
//     path and call process.exit() so we leave with the conventional 128+sig
//     status. The 'exit' listener also fires on those process.exit() calls,
//     but the cleanup already ran and the guard returns early.
//   - Windows TerminateProcess (no signal equivalent for POSIX SIGKILL) →
//     no Node handler runs; the 5-min in-progress notice TTL is the safety
//     net for this path.
let upgradeNoticeFinalized = false;
function clearAbortedUpgradeNotice() {
  if (!upgradeNoticeContext || upgradeNoticeFinalized) return;
  try { unlinkSync(UPGRADE_NOTICE_PATH()); } catch { /* already gone — fine */ }
}
process.on('exit', clearAbortedUpgradeNotice);
process.on('SIGTERM', () => { clearAbortedUpgradeNotice(); process.exit(143); });
process.on('SIGINT', () => { clearAbortedUpgradeNotice(); process.exit(130); });

// ── 0-bootstrap-sentinel. Surface partial-bootstrap failures (#975) ─────────
// `scripts/post-install-bootstrap.mjs` writes `.moflo/bootstrap-failed.json`
// when its file-sync left some helpers unwritten (WSL DrvFs lock, EBUSY race,
// breaker open, …). Without this block the user has no in-session signal
// that the upgrade was incomplete — the launcher itself ran fine, but it's
// running from STALE files. Emit a high-visibility line pointing them at
// the healer so the silent failure mode that produced #975 can't recur.
// Section 3h below clears the sentinel after a clean re-sync.
//
// Also write a `kind: 'repair'` upgrade-notice so the statusline surfaces
// the prompt persistently — emitWarning lands on stderr only and Claude Code
// relays it once on session start; the statusline keeps the indicator in
// front of the user until §3h flips it to `completed` (sync resolved) or
// the 5-min in-progress TTL expires (visibility cap, statusline tests).
let bootstrapSentinelData = null;
const BOOTSTRAP_SENTINEL_PATH = resolve(mofloDir(projectRoot), 'bootstrap-failed.json');
let bootstrapNoticeContext = null;
try {
  if (existsSync(BOOTSTRAP_SENTINEL_PATH)) {
    bootstrapSentinelData = JSON.parse(readFileSync(BOOTSTRAP_SENTINEL_PATH, 'utf-8'));
    const count = Array.isArray(bootstrapSentinelData?.failures) ? bootstrapSentinelData.failures.length : 0;
    const sentinelVersion = bootstrapSentinelData?.mofloVersion || 'unknown';
    emitWarning(
      `Upgrade detected ${count} unfinished install step(s) from npm install (moflo@${sentinelVersion}). Run /healer --fix to repair.`,
    );
    bootstrapNoticeContext = { kind: 'repair', from: sentinelVersion, to: sentinelVersion };
    buildAndWriteNotice(bootstrapNoticeContext, 'in-progress');
  }
} catch (err) {
  // Unreadable sentinel — leave it; healer will catch the underlying issue.
  emitWarning(`bootstrap sentinel read skipped (${errMessage(err)})`);
}

// ── 0. Legacy whole-DB / directory migrations have been retired (#851) ─────
// LEGACY-V2: Pre-#851 the launcher renamed `.claude-flow/` → `.moflo/` and
// byte-copied `.swarm/memory.db` → `.moflo/moflo.db` on every session start.
// Both blocks ran silently against a daemon that was still holding the old
// paths in memory, leaving consumers with ghost runtime files reappearing
// in legacy dirs and a `.gitignore` deletion that exposed 30+ daemon-state
// files for commit. See docs/moflo-4.9.1-upgrade-experience-2026-05-02.md
// for the full UX failure report.
//
// The version-bump-gated cherry-pick now lives inside section 3 (which is
// already the gate on the `.moflo/moflo-version` stamp). It stops the daemon
// first, then `INSERT OR IGNORE`s only the user-authored `learnings` /
// `knowledge` namespaces — every other DB row is derived and rebuilds via
// the indexers. LEGACY-V2 directories (`.swarm/`, `.claude-flow/`) are left
// in place as recovery sources; users delete them at their leisure.

// ── 0c. Memory DB index repair (#743) ───────────────────────────────────────
// The .moflo/moflo.db SQLite file accumulates index corruption ("row N missing
// from sqlite_autoindex_memory_entries_1") when sql.js's whole-file flush
// races with concurrent writes. Symptom is silent: indexers fail mid-write,
// the ephemeral-namespace purge (#729) silently no-ops, vector counts inflate.
//
// Probe + REINDEX in place. Must run BEFORE any sql.js consumer (the
// embeddings migration in 3e, the soft-delete + ephemeral purges in 3e-728/
// 3e-729, and the long-lived MCP server / daemon spawned in section 4) — all
// of those swallow corruption errors and silently drop work on the floor.
//
// Awaited because every downstream sql.js touch this session depends on a
// healthy index. Cost on the happy path is one PRAGMA check (~10ms).
try {
  const repair = await repairMemoryDbIfCorrupt(projectRoot);
  if (repair?.repaired) {
    // Three recovery tiers, three messages. Tier surfaces what level of
    // damage the DB had so the user (and any downstream telemetry) knows
    // whether row data was lost. See bin/lib/db-repair.mjs for the cascade.
    if (repair.tier === 'reindex') {
      emitMutation(
        'repaired memory db index',
        `${plural(repair.errors, 'index error')} fixed via REINDEX`,
      );
    } else if (repair.tier === 'vacuum') {
      emitMutation(
        'rebuilt memory db',
        `${plural(repair.errors, 'integrity violation')} fixed via VACUUM INTO; corrupt original kept at ${repair.corruptBackup ?? '.moflo/moflo.db.corrupt.*'}`,
      );
    } else if (repair.tier === 'salvage') {
      // Row-level salvage may have dropped rows; summarise loss so the
      // user sees what's gone before downstream consumers (indexer,
      // embeddings) re-process the survivors.
      let lossSummary = '';
      if (repair.lossStats) {
        const losses = Object.entries(repair.lossStats)
          .map(([tbl, s]) => {
            const lost = Math.max(0, s.read - s.written);
            return lost > 0 ? `${tbl} ${s.written}/${s.read}` : null;
          })
          .filter(Boolean);
        if (losses.length > 0) lossSummary = ` (rows preserved: ${losses.join(', ')})`;
      }
      emitMutation(
        'salvaged memory db',
        `${plural(repair.errors, 'integrity violation')} recovered via row-level salvage${lossSummary}; corrupt original kept at ${repair.corruptBackup ?? '.moflo/moflo.db.corrupt.*'}`,
      );
    } else {
      // Older db-repair without a `tier` field — fall back to legacy text.
      emitMutation(
        'repaired memory db',
        `${plural(repair.errors, 'integrity violation')} fixed`,
      );
    }
  } else if (repair?.persistent) {
    // Surface to stderr — Claude additionalContext + the user both see this.
    // Every recovery tier exhausted; user options are destructive only.
    process.stderr.write(
      `moflo: memory db has ${plural(repair.errors, 'integrity violation')} ` +
      `that REINDEX / VACUUM INTO / row-level salvage could not fix — ` +
      `run 'flo memory rebuild-index' (destructive) or restore from backup\n`,
    );
  }
} catch {
  // Non-fatal — repair is best-effort; downstream code paths report their
  // own errors if the DB is still broken.
}

// ── 0d. Silently clear post-install restart notice when version is current (#867, #887) ─
// scripts/post-install-notice.mjs drops `.moflo/restart-pending.json` on every
// `npm install moflo`. The UserPromptSubmit hook surfaces it on every prompt
// until cleared, so this session only sees the message between install and
// the FIRST restart that actually picks up the new bits.
//
// Cleanup is silent (#887): the user already saw + acted on the restart prompt
// — surfacing a "cleared notice" line on the very next session reads like an
// error in additionalContext and inflates mutationCount, which would also fire
// the closing "starting background tasks" framing. Both are noise on a
// successful post-restart session.
try {
  const pendingPath = join(mofloDir(projectRoot), 'restart-pending.json');
  const pkgPath = resolve(projectRoot, 'node_modules/moflo/package.json');
  const pending = JSON.parse(readFileSync(pendingPath, 'utf-8'));
  const installedVersion = JSON.parse(readFileSync(pkgPath, 'utf-8')).version;
  if (pending && typeof pending.version === 'string' && pending.version === installedVersion) {
    unlinkSync(pendingPath);
    try { unlinkSync(join(mofloDir(projectRoot), 'last-install-banner.json')); } catch { /* tracker may not exist */ }
  }
} catch { /* file missing or malformed — silent fast-path */ }

// ── 1. Helper: fire-and-forget a background process ─────────────────────────
function fireAndForget(cmd, args, label) {
  try {
    const proc = spawn(cmd, args, {
      cwd: projectRoot,
      stdio: 'ignore',     // Don't hold stdio pipes open
      detached: true,       // New process group
      shell: false,
      windowsHide: true     // No console popup on Windows
    });
    proc.unref();           // Let this process exit without waiting
  } catch {
    // If spawn fails (e.g. node not found), don't block startup
  }
}

// Cross-platform sync sleep — Atomics.wait parks the thread at the OS level
// without burning CPU (same primitive as src/cli/shared/utils/atomic-file-
// write.ts:131). Used by stopDaemon's liveness polling between graceful and
// forced termination so we never unlink the lockfile while the daemon is
// still running.
const STOP_SLEEP_BUF = new Int32Array(new SharedArrayBuffer(4));
function sleepSyncMs(ms) {
  Atomics.wait(STOP_SLEEP_BUF, 0, 0, ms);
}

// PID liveness check. EPERM means the process exists but is owned by another
// user — treat as alive (matches the canonical isAlive in process-manager.mjs
// after #1061; the prior `catch { return false; }` falsely reported foreign-
// owned daemons as dead and let the lockfile be unlinked under them).
//
// Linux zombie handling: on Linux, `kill(pid, 0)` returns success for zombie
// processes (exited but not yet reaped by their parent). A zombie can't write
// to the DB, hold locks, or do anything else stopDaemon cares about — treating
// it as alive exhausts the kill budget polling a corpse, then preserves the
// lockfile under a dead process. Production never hits this (the daemon is
// detached and reaped by init/systemd within ~ms), but a misbehaving parent
// can keep a daemon zombified, and the launcher's vitest harness reproduces
// the case deterministically (#1083 CI failure on ubuntu-latest). Read
// /proc/<pid>/stat (fixed-format, cheap) and treat 'Z' as dead.
function isDaemonPidAlive(pid) {
  try {
    process.kill(pid, 0);
  } catch (err) {
    return err && err.code === 'EPERM';
  }
  if (process.platform === 'linux') {
    try {
      const stat = readFileSync(`/proc/${pid}/stat`, 'utf-8');
      // Format: "pid (comm) state ..." — comm can contain spaces/parens, so
      // parse from the LAST ')' to skip it safely.
      const lastParen = stat.lastIndexOf(')');
      if (lastParen !== -1 && stat.charAt(lastParen + 2) === 'Z') return false;
    } catch (err) {
      // ENOENT = pid vanished between kill(0) and the read — already dead.
      if (err && err.code === 'ENOENT') return false;
      // Anything else (e.g. /proc unavailable) — keep the kill(0) verdict.
    }
  }
  return true;
}

// Stop the daemon recorded in `lockFile` (if any) without restarting. Used by
// the upgrade flow before any DB work — the daemon must not be holding old
// path resolution in memory, and a concurrent sql.js flush would clobber the
// cherry-picked rows. Returns true when a live PID was confirmed dead (or the
// PID was already gone when we read the lockfile).
//
// Escalation mirrors src/cli/commands/daemon.ts:killBackgroundDaemon so the
// launcher's upgrade path and `flo daemon stop` behave identically: graceful
// signal → wait → liveness check → force kill. The prior implementation sent
// bare `process.kill(pid, 'SIGTERM')` on every platform, which on Windows
// either silently force-kills or fails entirely depending on the process; in
// either case the catch swallowed the outcome and the lockfile was unlinked.
// The daemon (if it survived) then re-wrote the lockfile with its stale PID +
// pre-upgrade version, defeating the section-3a-pre version-skew recovery and
// leaving the statusline stuck on `📊 ?` until manual `flo daemon restart`.
//
// Section 4's `hooks.mjs session-start` spawn is responsible for starting a
// fresh daemon under the current code; this function intentionally does not.
function stopDaemon(lockFile) {
  if (!existsSync(lockFile)) return false;
  let stalePid = null;
  try {
    const lock = JSON.parse(readFileSync(lockFile, 'utf-8'));
    if (typeof lock?.pid === 'number' && lock.pid > 0) stalePid = lock.pid;
  } catch { /* malformed lock — fall through to unlink */ }

  let killed = false;
  if (stalePid !== null && isDaemonPidAlive(stalePid)) {
    // Platform-split shutdown. On Linux/macOS, SIGTERM lets the daemon's
    // shutdown handler run a final sql.js dump + lock release before we
    // escalate.
    //
    // On Windows there is no SIGTERM equivalent for our headless detached
    // Node daemon — `taskkill /PID` (no /F) sends a window-close message
    // that a non-GUI process can't receive and always fails with the visible
    // error 'process can only be terminated forcefully'. The prior
    // implementation invoked it anyway, swallowed the error, then polled
    // alive for 3s before escalating — exactly the time-waste that pushed
    // §3's stopDaemon past the 3000ms SessionStart hook timeout. Go
    // straight to /F /T (tree-kill, in case a worker child outlived its
    // parent) on Win.
    if (process.platform === 'win32') {
      try {
        execFileSync('taskkill', ['/F', '/T', '/PID', String(stalePid)], { windowsHide: true, timeout: 5000 });
      } catch { /* dead or unreachable — liveness poll below confirms */ }
      // Short grace period for OS reap (typically ~ms).
      const forceDeadline = Date.now() + 1000;
      while (Date.now() < forceDeadline) {
        if (!isDaemonPidAlive(stalePid)) { killed = true; break; }
        sleepSyncMs(100);
      }
    } else {
      try { process.kill(stalePid, 'SIGTERM'); } catch { /* signal failed — escalate below */ }

      // Poll for death up to 3s. The daemon's shutdown handler does a final
      // sql.js dump + lock release, which under load can take ~1s.
      const gracefulDeadline = Date.now() + 3000;
      while (Date.now() < gracefulDeadline) {
        if (!isDaemonPidAlive(stalePid)) { killed = true; break; }
        sleepSyncMs(100);
      }

      // Force-kill if still alive.
      if (!killed) {
        try { process.kill(stalePid, 'SIGKILL'); } catch { /* dead or unreachable */ }
        const forceDeadline = Date.now() + 1000;
        while (Date.now() < forceDeadline) {
          if (!isDaemonPidAlive(stalePid)) { killed = true; break; }
          sleepSyncMs(100);
        }
      }
    }

    if (!killed) {
      // Daemon survived both signals. Leave the lockfile in place so the next
      // session can see the stale PID and retry — unlinking now would let the
      // surviving daemon re-write the lockfile with its stale PID + version,
      // perpetuating the loop this fix exists to break.
      emitWarning(`stopDaemon: PID ${stalePid} did not exit after SIGTERM+force-kill; lockfile preserved`);
      return false;
    }
  } else if (stalePid !== null) {
    // PID was in the lockfile but the process is already gone — clean unlink.
    killed = true;
  }

  try { unlinkSync(lockFile); } catch { /* non-fatal */ }
  return killed;
}

// Stop-and-restart helper for the stale-daemon branch (section 3a-pre). The
// version-bump branch uses stopDaemon directly + relies on section 4 for the
// fresh start.
function recycleDaemon(lockFile, label) {
  const stopped = stopDaemon(lockFile);
  if (!stopped) return false;
  const localCliPath = resolve(projectRoot, 'node_modules/moflo/bin/cli.js');
  if (existsSync(localCliPath)) {
    fireAndForget('node', [localCliPath, 'daemon', 'start', '--quiet'], label);
  }
  return true;
}

// Numeric semver compare. Returns -1 / 0 / +1 for a vs b. Treats missing
// segments as 0 so '4.10' < '4.10.4'. Strips pre-release tags ('1.2.3-beta'
// compares as '1.2.3') — close enough for "is the daemon's version behind
// the installed package's version", which is all §2a needs.
function compareVersionsSemver(a, b) {
  const norm = (v) => String(v || '').split('-')[0].split('.').map((s) => {
    const n = parseInt(s, 10);
    return Number.isFinite(n) ? n : 0;
  });
  const aa = norm(a);
  const bb = norm(b);
  const len = Math.max(aa.length, bb.length);
  for (let i = 0; i < len; i++) {
    const av = aa[i] ?? 0;
    const bv = bb[i] ?? 0;
    if (av < bv) return -1;
    if (av > bv) return 1;
  }
  return 0;
}

